        <h4>What is copyright?</h4>

      <p>Copyright is a set of laws designed to give creators a package of rights over their original works of authorship.  These rights include:</p>
      <ul>
        <li>Creating derivative works or translations</li>
        <li>Reproducing the work in copies</li>
        <li>Distributing copies</li>
        <li>Performing or displaying publically or digitally</li>
        <li>Authorizing others to exercise any of these rights</li>
      </ul>

      <p>Copyright protects and balances the rights of the author and the public.  The law recognizes that creative works are unique, intellectual property with potential commerical value.  Additionally, it provides a legal framework to recover damages in cases of infringement and it provides an outline of acceptable methods of using protected works for many purposes.</p>
      </br>
      <h4>What does copyright protect?</h4>

      <p>According to the U.S. Copyright Office, “copyright protects ‘original works of authorship’ that are fixed in a tangible form of expression.”  In other words, the work must be written or recorded in some form to be protected.  The following types of works are identified:</p>
      <ul>

        <li>Literary works</li>
        <li>Musical works, including any accompanying words</li>
        <li>Dramatic works, including any accompanying music</li>
        <li>Pantomimes and choreographic works</li>
        <li>Pictorial, graphic, and sculptural works</li>
        <li>Motion pictures and other audiovisual works</li>
        <li>Sound recordings</li>
        <li>Architectural works</li>

        
      </ul>
      
      </br>
      <h4>What does copyright not protect?</h4>

      <p>Copyright does not protect the following:</p>
      <ul>

        <li>Works not fixed in a tangible form</li>
        <li>Ideas, facts or data</li>
        <li>Titles, names or slogans</li>
        <li>Works of the U.S. Government</li>
        <li>Works in the Public Domain</li>
        
      </ul>   

      </br>
      <h4>What is the Public Domain?</h4>

      <p>The Public Domain refers to works that were created in certain time periods that are no longer protected by copyright.  The following works are in the Public Domain:</p>
      <ul>

        <li>Works published prior to 1923</li>
        <li>Works published between 1923 and 1963 without a copyright notice</li>
        <li>Works published between 1923 and 1963 with a copyright notice but without the copyright being renewed</li>
        
      </ul>

      <p>*www.copyright.gov is a source to check the copyright status of works.  Renewed works will be registered here.  Please note: not all renewal records are available electronically.</p>

      <h4>What is Fair Use?</h4>

      <p>As previously mentioned each use must be analyzed on a case-by-case basis, but in general, Fair Use protects criticism, comment, news reporting, teaching, scholarship, and research.</p>

      <p>There are four factors that must be considered in determining whether or not a particular use is fair.  These factors are:</p>
      <ol>

        <li>The purpose and character of the use</li>
        <li>The nature of the copyrighted work</li>
        <li>The amount and substantiality of the portion used</li>
        <li>The effect of the use on the potential market for the copyright work</li>
        
      </ol>

      <p>For more detail concerning these four factors, as well as assistance in determining whether or not the use is fair, please see our <a href="http://www.lib.wayne.edu/services/research/copyright/checklist.php">Interactive Fair Use Checklist</a>.</p>

      </br>
      <h4>When should I seek permission?</h4>

      <p>You should seek copyright permission when you intend to use the work for commercial purposes, when you want to use a work in its entirety, or if the use does not comply with Fair Use statutes.</p>

      <p>The Wayne State University Library System can help you identify which materials are available through library resources, which materials would require permission to use, and help suggest alternative works that the library system owns.  Simply submit your course reading list using our <a href="http://www.lib.wayne.edu/forms/persistent_link_request.php">Persistent Link Request</a> form and a librarian will assist you.</p>

      </br>
      <h4>How do I obtain permission?</h4>

      <p>Once you have determined that you need to obtain copyright permission to use a work, search the Copyright Clearance Center at <a href="www.copyright.com">www.copyright.com</a>.</p>

      <p>If you would like further assistance, contact your <a href="http://www.lib.wayne.edu/info/staff/liaison.php">librarian liaison</a> or <a href="mailto:copyright@wayne.edu">copyright@wayne.edu</a>.</p>

<h4>What is the T.E.A.C.H. Act?</h4>

      <p>In 2002, Congress passed the Technology, Education, and Copyright Harmonization (T.E.A.C.H.) Act to address copyright concerns in online distance learning and course management systems.  Previously, copyright law strictly limited educators’ ability to remain compliant in an online setting.  The T.E.A.C.H. Act expands rights in the following ways:</p>
      <ul>

        <li>Transmission of performances of the entirety of a non-dramatic literary or musical work</li>
        <li>Transmission of reasonable and limited portions of any other performance</li>
        <li>Transmission of any work in amounts comparable to typical face-to-face displays</li>

        
      </ul>

      <p>In order to be T.E.A.C.H. Act compliant, there is a formidable list of conditions that must be met by the educational institution.  At this time, Wayne State University has not implemented the T.E.A.C.H. Act and WSU faculty are not able to use the act to justify their use of copyright materials within their online instruction.</p>

      <p>For more information on the T.E.A.C.H. Act, please see <a href="http://www.ala.org/advocacy/copyright/teachact/faq">The TEACH Act and some Frequently Asked Questions</a> as prepared by the <a href="http://www.ala.org/">American Library Association</a>.</p>

      <h4>How can the library help me?</h4>

      <p>The Wayne State University Library System has a number of online resources to help you answer your copyright questions.  We have developed a <a href="decisiontree.php">Copyright Decision Tree</a> as well as an <a href="checklist.php">Interactive Fair Use Checklist</a> to provide you with answers.  These resources, and many others, are located on our ©opyright @ Wayne State University site.</p>
      
      <p>Linking to resources that are available to your though the library system is the best way to ensure copyright compliance.  When you submit your course reading list using our <a href="http://www.lib.wayne.edu/forms/persistent_link_request.php">Persistent Link Request</a> form, a librarian will provide you with persistent links to these resources.</p>

      <p>As with any library questions you may have, you can always contact your <a href="http://www.lib.wayne.edu/info/staff/liaison.php">librarian liaison</a> with any copyright questions.  You may also contact <a href="mailto:copyright@wayne.edu">copyright@wayne.edu</a>.</p>

