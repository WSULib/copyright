        <h2>Copyright Guidelines for Posting Documents to Blackboard</h2>
        
<p><strong>Documents are no longer copyright protected, and therefore can be freely used, if:</strong></p>      

<ul>
<li>Published prior to 1923</li>
<li>Published between 1923 and 1963 without a copyright notice</li> 
<li>Published between 1923 and 1963 with copyright notice on work and not renewed with the <strong>U.S. Copyright Office</strong>
    <ul>
    <li>Note: The lack of online documentation with the <strong>U.S. Copyright Office</strong> does not mean the copyright was not renewed</li>
    </ul></li>
</ul>

<p><strong>Fair Use provisions and the Teach Act (2002) enables certain educational uses such as Blackboard posting, providing:</strong></p>

<ul>
<li>Limited portions of the work are made available at any one time</li> 
<li>Use must be part of mediated instructional activities</li>
<li>Availability must take place during a period of time relevant to the context and duration of a typical class session</li>
<li>Use must be limited to students enrolled in the course
    <ul>
    <li>Do not share work or distribute via email</li>
    <li>Ensure the Blackboard site is password protected</li>
    </ul></li>
<li>No item posted can be required reading in your course from within the last three years</li>
<li>No posting of textbook materials typically purchased or acquired by students is allowed or considered fair use</li>
<li>No posting of protected materials developed specifically for online use</li>
<li>No use of copies are allowed that are not directly captured from the licensed original</li>
</ul>

<p><strong>We recommend:</strong><p/>

<ul>
<li>Locate resources to use for coursework through full-text resources owned by the library system and link to them from Blackboard</li>
<li>Hyperlink to those resources from your digital syllabus</li>
<li>Reconsider the need to digitize a print resource and find a licensed full-text alternative</li>
<li>If you need to copy a resource:
    <ul>
    <li>Perform a fair use analysis, see: <a href="http://librarycopyright.net/fairuse/">http://librarycopyright.net/fairuse/</a> </li>
    <li>If deemed fair use, make a digital scan of a reasonable portion of the original</li>
    <li>Ensure a copyright notice is on the digital copy
        <ul>
        <li>The University Libraries will assist obtaining content if a request for a digital copy is placed</li> 
        <li>You can only post documents obtained through a delivery system if you or the library system has purchased the licensed original (meaning it is owned and the copy comes from the owned copy)</li></ul></li>
    </ul></li>
</ul>

<p><strong>All works not considered fair use require copyright permission. Obtaining copyright permission is the responsibility of the instructor posting on Blackboard.</strong></p>

<p><strong>We recommend:</strong></p>

<ul>
<li>Get permission from the rights-holder
<ul><li><a href="http://www.copyright.com">www.copyright.com</a> is the interface to seeking copyright permission</li>
</ul></li>
</ul>


